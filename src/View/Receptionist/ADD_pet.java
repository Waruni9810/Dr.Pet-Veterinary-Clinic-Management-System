    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked
String gender = null;
if (male.isSelected()) {
gender = "Male" ; 
} else if (female.isSelected()){
gender = "Female";
}

String cat = category.getSelectedItem().toString();

if (id.getText().trim().isEmpty() || name.getText().trim().isEmpty()||owner_contactno.getText().trim().isEmpty() ){
                 JOptionPane.showMessageDialog(null,"Add all the credentials", "Error" , JOptionPane.ERROR_MESSAGE);}
             else{
     Pet_RegisterController.Form(id.getText(), name.getText(),cat,gender,  age.getText() ,owner_name.getText(), owner_contactno.getText());
this.setVisible(false);
             }

    }//GEN-LAST:event_AddBtnMouseClicked

