    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
String gender = null;
if (male.isSelected()) {
gender = "Male" ; 
} else if (female.isSelected()){
gender = "Female";
}

//error message when not adding all the credentials 
if (id.getText().trim().isEmpty() || username.getText().trim().isEmpty()||password.getText().trim().isEmpty() ){
                 JOptionPane.showMessageDialog(null,"Add all the credentials", "Error" , JOptionPane.ERROR_MESSAGE);}
             else{
          //add records to the database
                 Pharmacist_RegisterController.Form(id.getText(), name.getText(),gender, NIC.getText(), contactno.getText(), username.getText(),password.getText());
clearField(); //clearfield
this.setVisible(false);
             }


    }//GEN-LAST:event_addMouseClicked

